package smartDevice;

public class smartPhone {
    public String marca;
    public String modelo;
    public int memoria;
    public double precio;
    
    public smartPhone() {}

    public smartPhone(String marca, String modelo, int memoria, double precio){
        this.marca=marca;
        this.modelo=modelo;
        this.memoria=memoria;
        this.precio=precio;
      
    }
}
